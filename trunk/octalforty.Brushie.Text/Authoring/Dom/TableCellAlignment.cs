namespace octalforty.Brushie.Text.Authoring.Dom
{
    /// <summary>
    /// Defines the alignment of table cells.
    /// <seealso cref="TableCell"/>
    /// </summary>
    public enum TableCellAlignment
    {
        /// <summary>
        /// Cell alignment is unknown.
        /// </summary>
        Unknown = 0,

        /// <summary>
        /// Align to the top.
        /// </summary>
        Top = 1,

        /// <summary>
        /// Align to the middle.
        /// </summary>
        Middle = 2,

        /// <summary>
        /// Align to the bottom.
        /// </summary>
        Bottom = 3
    }
}