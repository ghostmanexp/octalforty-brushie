namespace octalforty.Brushie.Text.Authoring.Textile
{
    /// <summary>
    /// Defines the alignment of a block-level Textile element.
    /// </summary>
    public enum BlockElementAlignment
    {
        /// <summary>
        /// Alignment is unknown.
        /// </summary>
        Unknown = 0,

        /// <summary>
        /// Left-aligned block.
        /// </summary>
        Left = 1,

        /// <summary>
        /// Right-aligned block.
        /// </summary>
        Right = 2,

        /// <summary>
        /// Centered block.
        /// </summary>
        Center = 3,

        /// <summary>
        /// Justified block.
        /// </summary>
        Justify = 4
    }
}
